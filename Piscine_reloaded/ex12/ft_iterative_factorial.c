int	ft_iterative_factorial(int nb)
{
	int	rt;

	if (nb < 0)
		return (0);
	rt = 1;
	while (nb > 1)
	{
		rt *= nb--;
		if (rt < 0)
			return (0);
	}
	return (rt);
}
