int	ft_count_if(char **tab, int (*f)(char*))
{
	int	rt;
	int	i;

	if (!tab || !f)
		return (0);
	rt = 0;
	i = -1;
	while (tab[++i])
		if (f(tab[i]) == 1)
			rt++;
	return (rt);
}
